# (0,0) (0,1)  (0,2)  (0,3) (0,4)  
# (1,0) (1,1)  (1,2)  (1,3) (1,4)
# (2,0) (2,1) '(2,2)' (2,3) (2,4)
# (3,0) (2,1)  (3,2)  (3,3) (3,4)
# (4,0) (4,1)  (4,2)  (4,3) (4,4)

# (2,2)을 기준으로 동, 북, 서, 남 표시는
dx = [0, -1, 0, 1]
dy = [1, 0, -1, 0]

# 현재 위치 
x,y = 2, 2

for i in range(4):
    # 동북서남의 위치 좌표 출력
    nx = x + dx[i]
    ny = y + dy[i]
    print(nx, ny) 